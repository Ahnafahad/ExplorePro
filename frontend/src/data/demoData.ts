// Helper to check if user is in demo mode
export const isDemoMode = (email?: string) => {
  if (!email) return false
  return email === 'demo.tourist@explorepro.com' ||
         email === 'demo.guide@explorepro.com' ||
         email === 'demo.admin@explorepro.com'
}
